package java1.Programming;

public class ClassApp {
    public static void main(String[] args) {
        // 수학과 관련된 클래스 : Math
        System.out.println(Math.PI);

        // 1.6이라는 값을 구했을 때 내림 or 올림처리하고 싶다
        System.out.println(Math.floor(1.6));            // Math.floor() : 내림
        System.out.println(Math.ceil(1.6));             // Math.ceil() : 올림
    }
}
