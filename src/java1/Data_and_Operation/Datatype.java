package java1.Data_and_Operation;

public class Datatype {
    public static void main(String[] args){
        System.out.println(6);                  // Data_and_Operation.Number
        System.out.println("six");              // String

        System.out.println("6");                // String

        System.out.println(6+6);                // 12
        System.out.println("6" + "6");          // 66

        System.out.println(6*6);                // 36
     // System.out.println("6"*"6");            // 문자열끼리 곱하는 건 안 됨
        System.out.println("1111".length());    // 문자열의 길이
     // System.out.println(1111.length());      // 정수형의 길이는 Length로 구할 수 없음
    }
}
